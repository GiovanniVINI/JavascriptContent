// Parâmetros de uma function

// Exemplo 1

function soma(a, b) {
    return a + b;
}

console.log('Resultado: ' + soma(2, 3)); 

// Exemplo 2

function somar(n1, n2) {
    let resultado = n1 + n2;
    console.log('Resultado: ' + resultado);
}

somar(10,20)

// Exemplo 3

function completeName(name, surname) {
    console.log(`${name} ${surname}`)
}

completeName('João', 'Silva')
