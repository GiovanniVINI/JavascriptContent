// Switch 

// Exemplo

var profession = 'Desenvolvedor Backend'

console.log(`Profissão: ${profession}`)

switch (profession) {
    case 'fiscal':
        console.log('Sua camisa é VERDE')
        break

    case 'bombeiro':
        console.log('Sua camisa é AMARELA')
        break
    
    default:
        console.log('Sua camisa é PRETA')
        break
}
